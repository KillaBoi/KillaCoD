void DoUnlockChallenge(int clientNum, const char* challengeName, const char* paramString)
{
	auto entity = pEntitiesList + 1448 * clientNum;

	if (!*(uintptr_t*)(entity + 0x150))
		return;

	auto param = strtok((char*)paramString, ",");

	if (SV_Challenges_BeginEvent(*(uintptr_t*)(entity + 0x150), SL_GetString(challengeName, 0))) {

		while (param != NULL) {

			SV_Challenges_AddParamString(param, strlen(param));

			param = strtok(NULL, ",");
		}

		SV_Challenges_FinishEvent();
	}
}

